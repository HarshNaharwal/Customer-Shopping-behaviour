-- QUESTION9. 
-- SEGMENT CUSTOMER INTO NEW , RETURNING AND LOYAL BASED ON THEIR TOTAL NUMBER OF PREVIOUS PURCHASE , 
-- AND SHOW THE COUNT OF EACH SEGMENT.
                
SELECT 
CASE WHEN previous_purchases='0' THEN 'New Customer'
     WHEN previous_purchases between 1 and 15 THEN 'Returning Customer'
ELSE 'Loyal Customers'
END AS customer_segment,
count(*) as customer_COUNT
FROM dbo.customer_behaviour_analysis
GROUP BY 
CASE WHEN previous_purchases='0' THEN 'New Customer'
     WHEN previous_purchases between 1 and 15 THEN 'Returning Customer'
ELSE 'Loyal Customers'
END

-- BUSINESS PROBLEM : LACK OF CUSTOMER SEGEMENTATION LEADS TO GENERIC STRATEGIES.

-- IMPACT : 1.ENABLES PERSONALIZED MARKETING.
--          2. IMPROVES RETENTION STRATEGIES.
