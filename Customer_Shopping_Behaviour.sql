-- QUESTION10. WHAT ARE THE TOP 3 MOST PURCHASED PRODUCTS WITHIN EACH CATEGORY ?
with cte as 
(
SELECT 
category,
item_purchased,
count(item_purchased) as total_purchased,
rank() over(partition by category order by count(item_purchased) DESC) as Rn
FROM dbo.customer_behaviour_analysis
GROUP BY category,item_purchased
)

SELECT *
FROM cte
WHERE Rn <= 3

-- ? BUSINESS PROBLEM:- THE COMPANY DOESN'T KNOW TOP - PERFORMING PRODUCTS WITHIN CATEGORIES.

-- IMPACT : 1.IMPROVES PRODUCT PLACEMENT AND RECOMMENDATIONS
--          2. HELPS INVENTORY OPTIMIZATION
--          3. INCREASES SALES THROUGH BEST- SELLERS  

--===============================================================================================================

-- QUESTION11. ARE CUSTOMERS WHO ARE REPEAT BUYERS (MORE THAN 5 PREVIOUS PURCHASES) ALSO LIKELY TO SUBSCRIBE ? 

SELECT 
CASE 
     WHEN previous_purchases > 5 THEN 'Repeat Buyers'
     ELSE 'Normal Buyers'
END as customer_type,
subscription_status,
count(*) as customer_count,
count(*) * 100.0 / sum(count(*)) over(partition by case 
                                       WHEN previous_purchases > 5 THEN 'Repeat Buyers'
                                       ELSE 'Normal Buyers'  
                                       END
                                       ) as percents
FROM dbo.customer_behaviour_analysis
GROUP BY 
CASE 
     WHEN previous_purchases > 5 THEN 'Repeat Buyers'
     ELSE 'Normal Buyers'
END ,subscription_status

