-- QUESTION 7. 
-- DO SUBSCRIBED CUSTOMERS SPEND MORE ? COMPARE AVERAGE SPEND AND TOTAL REVENUE BETWEEN SUBSCRIBED AND NON - SUBSCRIBERS. 

SELECT subscription_status,
count(customer_id) as users, 
ROUND(AVG(purchase_amount),2) as average_spend,
ROUND(sum(purchase_amount),2) as total_revenue
FROM customer_behaviour_analysis
GROUP BY subscription_status
ORDER BY average_spend desc ,total_revenue DESC,users DESC

-- ? BUSINESS PROBLEM : THE EFFECTIVENESS OF SUBSCRIPTION PROGRAMS IN UNKNOWN.

-- IMPACT : 1.VALIDATES SUBSCRIPTION MODEL PROFERMANCE.
--          2. IMPROVES CUSTOMER LOYALTY PROGRAMS
--          3. INCREASES CUSTOMER LIFETIME VALUE (CLV)    

-- QUESTION 8. TOP 5 PRODUCTS WITH THE HIGHEST DISCOUNT USAGE % .

SELECT 
TOP 5
item_purchased,
count(item_purchased) as total_number_of_times_sold,
count(CASE WHEN discount_applied='Yes' THEN 1 END) AS Number_of_times_sold_when_discount_applied,
count(CASE WHEN discount_applied='Yes' THEN 1 END) * 100.0 / count(*) as discount_percent
FROM dbo.customer_behaviour_analysis
GROUP BY item_purchased
ORDER BY discount_percent DESC

